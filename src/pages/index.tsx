// Next
import { type NextPage } from "next";
import Head from "next/head";
import { useSession } from "next-auth/react";

// React, hooks
import { useState } from "react";
import { useAnimation } from "@/hooks/useAnimation";

// Components
import DateTimer from "@/components/Timer/DateTimer";
import HomeTabs from "@/components/Tabs/HomeTabs";
import CalendarPopper from "@/components/Popper/CalendarPopper";

const Home: NextPage = () => {
  const { data: session } = useSession();
  const [isCalendarOpen, setIsCalendarOpen] = useState(false);
  const [shouldRender, animateTrigger, handleTransition] =
    useAnimation(isCalendarOpen);

  return (
    <>
      <Head>
        <title>Chill Today</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className="flex h-[85%] w-full flex-col">
        <div className="flex w-full justify-center">
          <DateTimer
            openCalendar={() => {
              setIsCalendarOpen(true);
            }}
          />
        </div>
        <div className="flex h-full w-full">
          <HomeTabs />
        </div>
        {shouldRender && (
          <CalendarPopper
            animateTrigger={animateTrigger}
            handleTransition={handleTransition}
            closeCalendar={() => {
              setIsCalendarOpen(false);
            }}
          />
        )}
      </main>
    </>
  );
};

export default Home;
